class User {
  final String name;
  final String caption;
  final String fans;
  final String rating;
  final String avatarUrl;
  final String trailingicon;

  User({
    required this.caption,
    required this.rating,
    required this.name,
    required this.fans,
    required this.avatarUrl,
    required this.trailingicon,
  });
}
